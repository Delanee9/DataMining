class BikeData {

    private final int hireTime;
    private final int bikeHires;

    public BikeData(int hireTime, int bikeHires) {
        this.hireTime = hireTime;
        this.bikeHires = hireTime;
    }

    public int getHireTime() {
        return hireTime;
    }

    public int getBikeHires() {
        return bikeHires;
    }
}